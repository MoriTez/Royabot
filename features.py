def zodiac_sign(day, month):
    signs = {
        (1, 20): ("جدی", "اهل کار، جدی و قابل اعتماد."),
        (2, 19): ("دلو", "خلاق، روشن‌فکر و آزادی‌خواه."),
        (3, 20): ("ماهی", "احساساتی، هنرمند و مهربان."),
        (4, 20): ("حمل", "پرانرژی، ریسک‌پذیر و شجاع."),
        (5, 21): ("ثور", "با اراده، صبور و واقع‌گرا."),
        (6, 21): ("جوزا", "باهوش، پرحرف و کنجکاو."),
        (7, 22): ("سرطان", "عاطفی، دلسوز و خانواده‌دوست."),
        (8, 23): ("اسد", "رهبر، مغرور و دوست‌داشتنی."),
        (9, 23): ("سنبله", "دقیق، اهل نظم و قابل اعتماد."),
        (10, 23): ("میزان", "متعادل، عاشق زیبایی و عدالت."),
        (11, 22): ("عقرب", "عمیق، مرموز و با اراده."),
        (12, 22): ("قوس", "جسور، ماجراجو و خوش‌بین."),
    }
    for (m, d), (name, desc) in reversed(sorted(signs.items())):
        if (month, day) >= (m, d):
            return name, desc
    return "جدی", "اهل کار، جدی و قابل اعتماد."

def countdown_to_100(birthdate):
    hundred = birthdate.replace(year=birthdate.year + 100)
    now = datetime.now()
    diff = hundred - now
    years_left = 100 - (now.year - birthdate.year)
    return f"{years_left} سال و {diff.days} روز تا ۱۰۰ سالگی‌ات مونده!"
