def zodiac_sign(day, month):
    zodiac = {
        (1, 20): "دی", (2, 19): "بهمن", (3, 20): "اسفند",
        (4, 20): "فروردین", (5, 21): "اردیبهشت", (6, 21): "خرداد",
        (7, 23): "تیر", (8, 23): "مرداد", (9, 23): "شهریور",
        (10, 23): "مهر", (11, 22): "آبان", (12, 22): "آذر"
    }
    for (m, d), name in sorted(zodiac.items()):
        if (month, day) <= (m, d):
            return name, zodiac_traits.get(name, "بی‌توضیح")
    return "دی", zodiac_traits["دی"]

def countdown_to_100(birthdate: datetime):
    now = datetime.now()
    death = birthdate.replace(year=birthdate.year + 100)
    remaining = death - now
    return f"{remaining.days} روز تا صدسالگی‌ات باقی مانده!"
